#input speed of car (miles per hour)
speed = int(input('Enter the speed at which a car was traveling in miles per hour: '))

#input time car traveled (hours)
time = int(input('Enter the number of hours the car has spent traveling: '))

#calculate distance
distance = speed * time

#output distance
print ('The total distance the car traveled is: ', distance)
           
